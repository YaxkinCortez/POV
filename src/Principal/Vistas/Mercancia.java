    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        if (Codigo.getText().equals("") || Producto.getText().equals("")
                || Precio.getText().equals("") || Stock.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Todos los campos\nson obligatorios.", "Productos", 0,
                    new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
        } else {
            Principal.Controlador.MercanciaControlador us = new MercanciaControlador();
            us.setPrimaryKey(Codigo.getText());
            us.setProducto(Producto.getText());
            us.setPrecio(Precio.getText());
            us.setStock(Stock.getText());
            int opcion = Principal.Modelos.MercanciaModelos.registrar(us);
            if (opcion != 0) {
                String id = Codigo.getText();
                selecionaFila(id);
                JOptionPane.showMessageDialog(this, "Registro éxitoso.", "Productos", 0,
                        new ImageIcon(getClass().getResource("/imagenes/verificar.png")));
            }
        }limpiaCampos();
    }//GEN-LAST:event_RegistrarActionPerformed

    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        if (TBproductos.getRowCount() > 0) {
            if (TBproductos.getSelectedRowCount() > 0) {
                if (Codigo.getText().equals("") || Producto.getText().equals("") || Stock.getText().equals("")
                        || Precio.getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Es necesario completar\ntodos los campos.", "Productos", 0,
                            new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
                } else if (JOptionPane.showConfirmDialog(this, "Esta a punto de actualizar\nun registro.\n¿Desea continuar?", "Productos", JOptionPane.YES_NO_OPTION, 0,
                        new ImageIcon(getClass().getResource("/imagenes/advertencia.png"))) == JOptionPane.YES_OPTION) {
                    Principal.Controlador.MercanciaControlador us = new MercanciaControlador();
                    us.setPrimaryKey(Codigo.getText());
                    us.setProducto(Producto.getText());
                    us.setPrecio(Precio.getText());
                    us.setStock(Stock.getText());
                    int opcion = Principal.Modelos.MercanciaModelos.actualizar(us);
                    if (opcion != 0) {
                        String id = Codigo.getText();
                        limpiaCampos();
                        selecionaFila(id);
                        JOptionPane.showMessageDialog(this, "Registro actualizado.", "Productos", 0,
                                new ImageIcon(getClass().getResource("/imagenes/actualizado.png")));
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un registro.", "Productos", 0,
                        new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
            }

        } else {
            JOptionPane.showMessageDialog(this, "No hay registros\npara actualizar.", "Productos", 0,
                    new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
        }
    }//GEN-LAST:event_ActualizarActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        if (TBproductos.getRowCount() > 0) {
            if (TBproductos.getSelectedRowCount() > 0) {
                if (JOptionPane.showConfirmDialog(this, "Esta a punto de eliminar\nun registro.\n¿Desea continuar?", "Productos", JOptionPane.YES_NO_OPTION, 0,
                        new ImageIcon(getClass().getResource("/imagenes/advertencia.png"))) == JOptionPane.YES_OPTION) {
                    int fila = TBproductos.getSelectedRow();
                    String id = TBproductos.getValueAt(fila, 0).toString();
                    int elimina = Principal.Modelos.MercanciaModelos.eliminar(id);
                    if (elimina != 0) {
                        limpiaCampos();
                        JOptionPane.showMessageDialog(this, "Registro eliminado.", "Productos", 0,
                                new ImageIcon(getClass().getResource("/imagenes/papelera.png")));
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Seleccione un registro.", "Productos", 0,
                        new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
            }

        } else {
            JOptionPane.showMessageDialog(this, "No hay registros\npara eliminar.", "Productos", 0,
                    new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
        }
    }//GEN-LAST:event_EliminarActionPerformed

    private void EliminarBaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarBaseActionPerformed
        if (TBproductos.getRowCount() > 0) {
            if (JOptionPane.showConfirmDialog(this, "Esta a punto de elimnar\ntodos los registros.\n¿Desea continuar?", "Productos", JOptionPane.YES_NO_OPTION, 0,
                    new ImageIcon(getClass().getResource("/imagenes/advertencia.png"))) == JOptionPane.YES_OPTION) {
                int eliminaT = Principal.Modelos.MercanciaModelos.eliminaTodos();
                if (eliminaT != 0) {
                    limpiaCampos();
                    JOptionPane.showMessageDialog(this, "Registros eliminados.", "Productos", 0,
                            new ImageIcon(getClass().getResource("/imagenes/borrar.png")));
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay registros\npara eliminar.", "Productos", 0,
                    new ImageIcon(getClass().getResource("/imagenes/informacion.png")));
        }
    }//GEN-LAST:event_EliminarBaseActionPerformed

