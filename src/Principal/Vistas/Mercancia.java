    void cambiaDatos() {
        int fila = TBproductos.getSelectedRow();
        Codigo.setText(TBproductos.getValueAt(fila, 0).toString());
        Producto.setText(TBproductos.getValueAt(fila, 1).toString());
        Precio.setText(TBproductos.getValueAt(fila, 2).toString());
        Stock.setText(TBproductos.getValueAt(fila, 3).toString());

    }

    void limpiaCampos() {
        if (TBproductos.getSelectedRow() > -1) {
            TBproductos.removeRowSelectionInterval(TBproductos.getSelectedRow(), TBproductos.getSelectedRow());
        }
        Codigo.setText("");
        Producto.setText("");
        Precio.setText("");
        buscar.setText("");
        Stock.setText("");
        Principal.Modelos.MercanciaModelos.listar("");
    }

    void selecionaFila(String id) {
        for (int i = 0; i < TBproductos.getRowCount(); i++) {
            if (id.equals(TBproductos.getValueAt(i, 0))) {
                TBproductos.setRowSelectionInterval(i, i);
                break;
            }
        }

    }

