    private void agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarActionPerformed
        if (TBlistar.getRowCount() > 0) {
            try {
                String cant = null;
                DefaultTableModel tabladet = (DefaultTableModel) Principal.Vistas.Caja.TBcaja.getModel();

                String[] dato = new String[4];

                int fila = TBlistar.getSelectedRow();

                if (fila == -1) {
                    JOptionPane.showMessageDialog(this, "Seleccione un registro.", "Mercancia", 0,
                            new ImageIcon(getClass().getResource("/imagenes/info.png")));
                } else {
                    String cod = TBlistar.getValueAt(fila, 0).toString();
                    String nom = TBlistar.getValueAt(fila, 1).toString();
                    String precio = TBlistar.getValueAt(fila, 2).toString();
                    int stockActual = Integer.parseInt(TBlistar.getValueAt(fila, 3).toString());
                    int c = 0;
                    int j = 0;
                    cant = JOptionPane.showInputDialog(this, "Cantidad:", "Mercancia", JOptionPane.INFORMATION_MESSAGE);
                    while (!CajaModelo.isNumber(cant) && cant != null) {
                        cant = JOptionPane.showInputDialog(this, "Debe ingresar valores numÃ©ricos\ny que sean mayor a 0:",
                                "Error", JOptionPane.ERROR_MESSAGE);
                    }
                    if ((cant.equals("")) || (cant.equals("0"))) {
                        JOptionPane.showMessageDialog(this, "Debe ingresar algun valor mayor que 0");
                    } else if (Integer.parseInt(cant) > stockActual) {
                        JOptionPane.showMessageDialog(null, "La cantidad maxima a vender es: " + stockActual);
                    } else {
                        for (int i = 0; i < Principal.Vistas.Caja.TBcaja.getRowCount(); i++) {
                            Object com = Principal.Vistas.Caja.TBcaja.getValueAt(i, 0);
                            Object cant1 = Principal.Vistas.Caja.TBcaja.getValueAt(i, 2);
                            if (cod.equals(com)) {
                                j = i;
                                int cantT = Integer.parseInt(cant) + Integer.parseInt((String) cant1);
                                Principal.Vistas.Caja.TBcaja.setValueAt(String.valueOf(cantT), i, 2);
                                c++;
                                calcular();
                                Principal.Vistas.Caja.Recibe.setText("");
                                //ca.cambio.setText("");
                            }
                        }
                        if (c == 0) {

                            dato[0] = cod;
                            dato[1] = nom;
                            dato[2] = cant;
                            dato[3] = precio;

                            tabladet.addRow(dato);

                            Principal.Vistas.Caja.TBcaja.setModel(tabladet);
                            calcular();

                            Principal.Vistas.Caja.Recibe.setText("");
                            //CajaAd.cambio.setText("");
                        }
                    }
                }
            } catch (Exception e) {
            }
        } else {
            JOptionPane.showMessageDialog(this, "No hay registros.", "Mercancia", 0,
                    new ImageIcon(getClass().getResource("/imagenes/info.png")));
        }
    }//GEN-LAST:event_agregarActionPerformed

    public void calcular() {
        String pre;
        String can;
        double total = 0;
        double precio;
        int cantidad;
        double imp = 0.0;

        for (int i = 0; i < Principal.Vistas.Caja.TBcaja.getRowCount(); i++) {
            can = Principal.Vistas.Caja.TBcaja.getValueAt(i, 2).toString();
            pre = Principal.Vistas.Caja.TBcaja.getValueAt(i, 3).toString();
            precio = Double.parseDouble(pre);
            cantidad = Integer.parseInt(can);
            imp = precio * cantidad;
            total = total + imp;
            Principal.Vistas.Caja.TBcaja.setValueAt(Math.rint(imp * 100) / 100, i, 4);

        }
        Principal.Vistas.Caja.Total.setText("" + Math.rint(total * 100) / 100);

    }

